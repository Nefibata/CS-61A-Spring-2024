def falling(n, k):
    """Compute the falling factorial of n to depth k.

    >>> falling(6, 3)  # 6 * 5 * 4
    120
    >>> falling(4, 3)  # 4 * 3 * 2
    24
    >>> falling(4, 1)  # 4
    4
    >>> falling(4, 0)
    1
    """
    "*** YOUR CODE HERE ***"
    if n==0 or k ==0 :
        return 1
    else :
        re = n
        x=n
        while k>1:
            k=k-1
            x=x-1
            re=re*x
        return re 
    
        



